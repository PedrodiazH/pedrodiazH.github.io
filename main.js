/* Esta parte es para actualizar contenido diariamente del html,
en dirección al sitio web*/
let indicePalabraActual = 0;
const palabraElement = document.getElementById("palabra");
const definicionElement = document.getElementById("definicion");
const usoElement = document.getElementById("uso");

function cambiarPalabra(){
    palabraElement.textContent = jsonData[indicePalabraActual].palabra;
    definicionElement.textContent = jsonData[indicePalabraActual].definicion;
    usoElement.textContent = jsonData[indicePalabraActual].uso;

    indicePalabraActual = (indicePalabraActual + 1) % jsonData.length
    /* es como un ciclo en donde el incremento es +1, de esta forma la palabra cambia
    mediante el numero q se le asigna, %jsonData.length es para q al acabar el ciclo en
    el tamaño del json, este vuelva al comienzo. */
}
/*  esto garantiza que las palabras se muestren en el orden 
correspondiente a sus números en el JSON */
jsonData.sort((a, b) => a.numero - b.numero);

cambiarPalabra(); // llamar función
setInterval(cambiarPalabra, 5000); // prueba cada 5 segundos
